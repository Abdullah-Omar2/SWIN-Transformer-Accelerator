    logic signed [WIDTH-1:0] din  [0:SIZE-1];
    logic signed [WIDTH-1:0] dout [0:SIZE-1];

    // ??????? ??? DUT
    relu #(
        .WIDTH(WIDTH),
        .SIZE(SIZE)
    ) dut (
        .din(din),
        .dout(dout)
    );

