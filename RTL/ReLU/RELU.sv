module relu #(
    parameter int WIDTH  = 16,   // ??? ?????? ??? ????
    parameter int SIZE   = 8     // ??? ??????? ?? ??? vector
) (
    input  logic signed [WIDTH-1:0] din   [0:SIZE-1],  // vector ?? ????????
    output logic signed [WIDTH-1:0] dout  [0:SIZE-1]   // vector ?? ????????
);

    genvar i;
    generate
        for (i = 0; i < SIZE; i++) begin : relu_loop
            always_comb begin
                if (din[i] < 0)
                    dout[i] = '0;   // ?? ?????? ????? ???
                else
                    dout[i] = din[i]; // ?? ???? ?? ???? ???? ??? ??
            end
        end
    endgenerate

endmodule

